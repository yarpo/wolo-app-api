package pl.pjwstk.woloappapi.model.translation;

public class ReportTranslationResponce {
    private String reportPL;
    private String reportEN;
    private String reportUA;
    private String reportRU;

    public ReportTranslationResponce() {
    }

    public String getReportPL() {
        return reportPL;
    }

    public void setReportPL(String reportPL) {
        this.reportPL = reportPL;
    }

    public String getReportEN() {
        return reportEN;
    }

    public void setReportEN(String reportEN) {
        this.reportEN = reportEN;
    }

    public String getReportUA() {
        return reportUA;
    }

    public void setReportUA(String reportUA) {
        this.reportUA = reportUA;
    }

    public String getReportRU() {
        return reportRU;
    }

    public void setReportRU(String reportRU) {
        this.reportRU = reportRU;
    }
}
