package pl.pjwstk.woloappapi.model.translation;

public class ShiftTranslation {
    private String addressDescriptionPL;
    private String addressDescriptionEN;
    private String addressDescriptionUA;
    private String addressDescriptionRU;

    public ShiftTranslation() {
    }

    public String getAddressDescriptionPL() {
        return addressDescriptionPL;
    }

    public void setAddressDescriptionPL(String addressDescriptionPL) {
        this.addressDescriptionPL = addressDescriptionPL;
    }

    public String getAddressDescriptionEN() {
        return addressDescriptionEN;
    }

    public void setAddressDescriptionEN(String addressDescriptionEN) {
        this.addressDescriptionEN = addressDescriptionEN;
    }

    public String getAddressDescriptionUA() {
        return addressDescriptionUA;
    }

    public void setAddressDescriptionUA(String addressDescriptionUA) {
        this.addressDescriptionUA = addressDescriptionUA;
    }

    public String getAddressDescriptionRU() {
        return addressDescriptionRU;
    }

    public void setAddressDescriptionRU(String addressDescriptionRU) {
        this.addressDescriptionRU = addressDescriptionRU;
    }
}
